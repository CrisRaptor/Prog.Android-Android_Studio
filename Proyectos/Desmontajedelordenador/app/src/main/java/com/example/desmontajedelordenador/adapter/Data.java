package com.example.desmontajedelordenador.adapter;

public class Data {
    private int imagen;
    private String nombre;

    public Data(int imagen, String nombre) {
        this.imagen = imagen;
        this.nombre = nombre;
    }

    public int getImagen() {
        return imagen;
    }

    public String getNombre() {
        return nombre;
    }
}
